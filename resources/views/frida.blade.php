<h2>Frida Kahlo</h2>

<p> Magdalena Carmen Frida Kahlo Calderón (Coyoacán, 6 de julio de 1907 - 13 de julio de 1954) 
     fue una pintora mexicana.​ Su vida estuvo marcada por el infortunio de sufrir un grave accidente 
     en su juventud que la mantuvo postrada en cama durante largos periodos, llegando a someterse hasta 
     a 32 operaciones quirúrgicas. Llevó una vida poco convencional. Su obra pictórica gira temáticamente 
     en torno a su biografía y a su propio sufrimiento. Fue autora de 150 obras, principalmente autorretratos,
     en los que proyectó sus dificultades por sobrevivir. La obra de Frida y la de su marido, el pintor 
     Diego Rivera, se influyeron mutuamente. Ambos compartieron el gusto por el arte popular mexicano de 
     raíces indígenas, inspirando a otros pintores mexicanos del periodo posrevolucionario. </p>

<p> En 1939 expuso sus pinturas en Francia gracias a una invitación de André Breton, quien intentó convencerla 
     de que eran «surrealistas», aunque Kahlo decía que esta tendencia no correspondía con su arte ya que ella 
     no pintaba sueños sino su propia vida. Una de las obras de esta exposición (Autorretrato-El marco, que 
     actualmente se encuentra en el Centro Pompidou) se convirtió en el primer cuadro de un artista mexicano 
     dquirido por el Museo del Louvre. Hasta entonces, Frida Kahlo había pintado solo privadamente y a ella 
     misma le costó admitir que su obra pudiese tener un interés general. Aunque gozó de la admiración de 
     destacados pintores e intelectuales de su época como Pablo Picasso, Vasili Kandinski, André Breton o 
     Marcel Duchamp, su obra alcanzó fama y verdadero reconocimiento internacional después de su muerte, 
     especialmente a finales de los años de 1980 y principios de 1990 por coleccionistas como la cantante Madonna. </p>

