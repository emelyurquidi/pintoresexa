<h2>Pablo Diego José Ruiz Picasso</h2>

<p> Figura excepcional como artista y como hombre, Picasso fue protagonista y 
    creador inimitable de las diversas corrientes que revolucionaron las artes 
    plásticas del siglo XX, desde el cubismo hasta la escultura neofigurativa, 
    del grabado o el aguafuerte a la cerámica artesanal o a la escenografía para 
    ballets. Su obra inmensa en número, en variedad y en talento se extiende a lo 
    largo de más de setenta y cinco años de actividad creadora, que el pintor 
    compaginó sabiamente con el amor, la política, la amistad y un exultante y 
    ontagioso goce de la vida. Famoso desde la juventud, admirado y solicitado 
    por los célebres y poderosos, fue esencialmente un español sencillo, saludable 
    y generoso, dotado de una formidable capacidad de trabajo, enamorado de los barrios 
    bohemios de París, del sol del Mediterráneo, de los toros, de la gente sencilla y 
    de las mujeres hermosas, afición que cultivó sin desmayo.

Pablo Diego José Ruiz Picasso, conocido luego por su segundo apellido, nació el 25 
de octubre de 1881, en el n.º 36 de la plaza de la Merced de Málaga, como primogénito 
del matrimonio formado por el pintor vasco José Ruiz Blasco y la andaluza María Picasso 
López. El padre era profesor de dibujo en la Escuela Provincial de Artes y Oficios, 
conocida como Escuela San Telmo. La primera infancia de Pablo transcurrió entre las 
dificultades económicas de la familia y una estrecha relación entre padre e hijo, que 
ambos cultivaban con devoción. El niño era un escolar menos que discreto, bastante 
perezoso y muy distraído, pero con precoz facilidad para el dibujo, que don José estimulaba. </p>

<p>En 1891 la familia se traslada a La Coruña, en cuyo Instituto da Guarda son requeridos 
los servicios del padre como profesor. Pablo inicia sus ensayos pictóricos, y tres años 
más tarde su progenitor y primer maestro le cede sus propios pinceles y caballetes, admirado 
ante el talento de su hijo. En 1895, Ruiz Blasco obtiene un puesto docente en la 
Escola d'Arts i Oficis de la Llotja de Barcelona. Pablo resuelve en un día los ejercicios 
de examen previstos para un mes, y es admitido en la escuela. En 1896, con sólo quince años, 
instala su primer taller en la calle de la Plata de la Ciudad Condal.

Dos años más tarde obtiene una mención honorífica en la gran exposición de Madrid por su obra 
Ciencia y caridad, todavía de un realismo académico, en la que el padre ha servido de modelo 
para la figura de un médico. La distinción lo estimula a rendir oposición al curso adelantado 
en la Academia de San Fernando, mientras sus trabajos, influidos por El Greco y Toulouse-Lautrec, 
obtienen nuevas medallas en Madrid y Málaga.</p>

<p>En 1898 realiza su primera muestra individual en Els Quatre Gats de Barcelona. Finalmente, 
en el otoño del año 1900 hace una visita a París para ver la Exposición Universal. Allí vende 
tres dibujos al marchante Petrus Mañach, quien le ofrece 150 francos mensuales por toda su obra 
de un año. Pablo es ya un artista profesional, y decide firmar sólo con el apellido materno. En 
1901 coedita en Madrid la efímera revista Arte Joven, y en marzo viaja nuevamente a París, donde 
conoce a Max Jacob y comienza lo que luego se llamará su «período azul». Al año siguiente expone 
su primera muestra parisiense en la galería de Berthe Weill, y en 1904 decide trasladarse 
definitivamente a la capital francesa.

Picasso se instala en el célebre Bateau-Lavoir, en el número 13 de la calle Ravignan (hoy plaza Hodeau), 
alojamiento variadamente compartido por artistas sin blanca, entre otros el también español Juan Gris. 
Allí trabó amistad con Braque y Apollinaire, y se enamoró de Fernanda Olivier. Durante tres años Picasso 
pinta y dibuja sin cesar, rendido a la influencia de Cézanne, mientras elabora con Braque las líneas maestras 
del cubismo analítico, cuya gran obra experimental, Las señoritas de Aviñón, es pintada por Picasso en 1907.</p>

<p>Pronto sobreviene el asombro y el escándalo ante un estilo deforme que rompe todos los cánones y va ganando 
nuevos adeptos, al tiempo que su audaz inventor expone en Munich (1909) y en Nueva York (1911). Pablo ha 
encontrado una nueva compañera en Marcelle Humbert, y siempre seguido por Braque, se lanza a crear el cubismo 
sintético, que los acerca al borde de la abstracción (en su extensa y tan variada obra, Picasso jamás llegaría 
a abandonar la figuración). Poco después se muda de Montmartre a Montparnasse, y se abren exposiciones suyas en 
Londres y Barcelona.

En 1914, con el estallido de la Primera Guerra Mundial, llegan las tragedias: Braque y Apollinaire son movilizados, 
y Marcelle muere súbitamente ese otoño. Pablo abandona prácticamente el cubismo, y busca otros caminos artísticos. 
Los encuentra en 1917, cuando por medio de Jean Cocteau conoce a Serguéi Diáguilev, que le encarga los decorados del 
ballet Parade de Erik Satie. El fin de la guerra le trae un nuevo amor, la bailarina Olga Clochlova, y también un 
nuevo dolor: la muerte de Apollinaire a consecuencia de una grave herida en la cabeza. Se casa con Olga en 1918, y 
hasta 1925 trabaja en diversos ballets que dan cauce a su evolución pictórica.
</p>

<p>Un viejo retrato de su madre, pintado en 1918, le valdrá el millonario premio Carnegie de 1930, que le permite 
adquirir una suntuosa villa campestre en Boisgelup, y pasarse más de un año viajando por España. Por entonces 
vuelve a la escultura y mantiene un romance con Teresa Walter, del que nace su primera hija, Maya. La Clochlova 
inicia un escandaloso juicio para conseguir el divorcio, que el juez se niega a conceder. Despechado, Picasso 
se enamora de Dora Maar.

Al estallar la Guerra Civil, Picasso apoya con firmeza al bando republicano, y acepta simbólicamente la dirección 
del museo del Prado, mientras en 1937 pinta el Guernica en París. Dos años después se realiza una gran exposición 
antológica en el Museo de Arte Moderno de Nueva York. Deprimido por el triunfo de los nacionales y la posterior 
ocupación de Francia por los nazis, pasa la mayor parte de la Segunda Guerra Mundial trabajando en su refugio 
de Royan.</p>

<p> En 1944 se afilia al Partido Comunista Francés y da a conocer 77 nuevas obras en el Salón de Otoño. Después se 
entusiasma por la litografía y por la joven y hermosa pintora Françoise Guillot, con la que convive hasta 1946. 
Se inicia así su etapa de Vallauris, en la que trabaja en sus magníficas cerámicas. Con Françoise tendrá dos 
hijos: Claude, nacido en 1947, y Paloma en 1949.

En 1954, el infatigable anciano se fascina por una misteriosa adolescente de delicado perfil y largos cabellos 
rubios llamada Sylvette David, que acepta posar para él a cambio de uno de los retratos, a su elección. El trato 
se cumple y su resultado produce algunas de las obras más conocidas y reproducidas del pintor, como el famoso 
perfil de Sylvette en la butaca verde.

Si la fascinación por la etérea Sylvette había sido platónica, no tuvo el mismo cariz su atracción por Jacqueline 
Roqué, joven de extraodinaria belleza a la que tomó como compañera en 1957, un año antes de pintar el gigantesco 
mural para la UNESCO. Fértil milagro del arte y de la vida, Picasso seguiría creando, amando, trabajando y 
viviendo intensamente hasta morir en 1973. Dejó tras de sí la mayor y más rica obra artística personal de 
nuestro siglo, y una fabulosa herencia que provocó agrias disputas hasta recaer en un ser de pacífico 
nombre: su hija Paloma.  </p>

